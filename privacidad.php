<h4>GARANTIA DE PRIVACIDAD PARA LOS DATOS DE LOS USUARIOS DE
NUESTRO SITIO DE INTERNET</h4>
        <p>
          AUTOPAWN S.A. DE C.V. (“AUTOPAWN TIJUANA”) garantiza la protección
a todos nuestros usuarios de los datos que nos proporcione en nuestro sitio
de internet que en adelante se le identificara con la denominación del “Sitio”,
respetando siempre los derechos otorgados por las leyes que rigen sobre la
materia.
        </p>

        <p>
Todos los datos que AQUÍ se recaben de los usuarios de nuestro sitio, serán
tratados con absoluta confidencialidad, de total forma AUTOPAWN S.A. DE
C.V, garantiza que los datos proporcionados están siendo utilizados única y
exclusivamente para los fines para los cuales se proporcionaron y que son
solicitados en nuestro sitio, atendiendo lo anterior a lo dispuesto por la legislación
mexicana. Por lo anterior, AUTOPAWN S.A. DE C.V se obliga a custodiar y
utilizar los datos personales brindados por nuestros usuarios para los fines que se
señalan a continuación:
        </p>

        <ol>
        	<li>La identificación y el conocimiento del o la titular de los datos personales en
adelante “El Titular”.</li>
        	<li>Para la prevención y/o la detección de posibles fraudes u otros ilícitos en
agravio de AUTOPAWN S.A. DE C.V y/o el Titular.</li>
        	<li>b) Realizar las consultas necesarias en las Sociedades de Información
Crediticia e Entidades Públicas de la información e historial crediticio del o
la Titular.</li>
        	<li>Para la publicidad, promoción y/o telemarketing al o la titular del vehículo,
de los servicios que sean ofertados por AUTOPAWN S.A. DE C.V.</li>
			<li>Con fines de mercadotecnia, y/o estadística, para el direccionamiento de
nuestras campañas publicitarias, para el reporteo de AUTOPAWN S.A. DE
C.V. Con el fin de detectar las necesidades de nuestros Usuarios y
ofrecerles los servicios o productos más adecuado a sus necesidades</li>
			<li>Confirmar y/o corregir información proporcionada por nuestros clientes y
usuarios de nuestro sitio.</li>
			<li>Mejorar nuestros procesos frente a nuestros clientes y los usuarios del sitio</li>
			<li>Para auditar y revisar nuestras actividades, y mejorar nuestros procesos
administrativos internos.</li>
			<li>Para realizar investigaciones de mercado orientadas a mejoras nuestros
servicios y productos.</li>
			<li>Con la única finalidad brindar un mejor servicio a nuestros clientes y
usuarios.</li>
        </ol>

        <p>
